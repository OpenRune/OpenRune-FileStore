    var type: Int? = null
    var buttonType: Int? = null
    var contentType: Int? = null
    var x: Int? = null
    var y: Int? = null
    var width: Int? = null
    var height: Int? = null
    var trans1: Int? = null
    var layer: Int? = null
    var mouseOverRedirect: Int? = null
    var cs1Comparisons: ShortArray? = null
    var cs1ComparisonValues: IntArray? = null
    var cs1Instructions: Array<IntArray>? = null
    var scrollHeight: Int? = null
    var hide: Boolean? = null
    var fill: Boolean? = null
    var textAlignH: Int? = null
    var textAlignV: Int? = null
    var textLineHeight: Int? = null
    var textFont: Int? = null
    var textShadow: Boolean? = null
    var secondaryText: String? = null
    var colour1: Int? = null
    var colour2: Int? = null
    var mouseOverColour1: Int? = null
    var mouseOverColour2: Int? = null
    var graphic: Int? = null
    var secondaryGraphic: Int? = null
    var modelKind: Int? = null
    var model: Int? = null
    var secondaryModelKind: Int? = null
    var secondaryModel: Int? = null
    var modelAnim: Int? = null
    var secondaryModelAnim: Int? = null
    var modelZoom: Int? = null
    var modelAngleX: Int? = null
    var modelAngleY: Int? = null
    var text: String? = null
    var targetVerb: String? = null
    var targetBase: String? = null
    var events: Int? = null
    var buttonText: String? = null
    var widthMode: Int? = null
    var heightMode: Int? = null
    var xMode: Int? = null
    var yMode: Int? = null
    var scrollWidth: Int? = null
    var noClickThrough: Boolean? = null
    var angle2d: Int? = null
    var tiling: Boolean? = null
    var outline: Int? = null
    var graphicShadow: Int? = null
    var vFlip: Boolean? = null
    var hFlip: Boolean? = null
    var modelX: Int? = null
    var modelY: Int? = null
    var modelAngleZ: Int? = null
    var modelOrthog: Boolean? = null
    var modelObjWidth: Int? = null
    var lineWid: Int? = null
    var lineDirection: Boolean? = null
    var opBase: String? = null
    var op: Array<String> = arrayOf()
    var dragDeadZone: Int? = null
    var dragDeadTime: Int? = null
    var draggableBehavior: Boolean? = null
    var onLoad: Array<Any>? = null
    var onMouseOver: Array<Any>? = null
    var onMouseLeave: Array<Any>? = null
    var onTargetLeave: Array<Any>? = null
    var onTargetEnter: Array<Any>? = null
    var onVarTransmit: Array<Any>? = null
    var onInvTransmit: Array<Any>? = null
