        const val DEFAULT_LINE_WID: Int = 1
        const val DEFAULT_GRAPHIC: Int = -1
        const val DEFAULT_SECONDARY_GRAPHIC: Int = -1
        const val DEFAULT_MODEL_KIND: Int = 1
        const val DEFAULT_MODEL: Int = -1
        const val DEFAULT_SECONDARY_MODEL_KIND: Int = 1
        const val DEFAULT_SECONDARY_MODEL: Int = -1
        const val DEFAULT_MODEL_ANIM: Int = -1
        const val DEFAULT_SECONDARY_MODEL_ANIM: Int = -1
        const val DEFAULT_MODEL_ZOOM: Int = 100
        const val DEFAULT_TEXT_FONT: Int = -1
        const val DEFAULT_MOUSE_OVER_REDIRECT: Int = -1
        const val DEFAULT_BUTTON_TEXT: String = "Ok"
        const val DEFAULT_LAYER: Int = -1
        const val DEFAULT_EMPTY_TEXT: String = ""
